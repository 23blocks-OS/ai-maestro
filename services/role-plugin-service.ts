/**
 * Install a role-plugin into an agent's working directory with local scope.
 * This creates <agentDir>/.claude/settings.local.json with the plugin enabled,
 * making it visible only to that agent's Claude Code session.
 *
 * @param pluginName - The plugin to install
 * @param agentDir - The agent's working directory (persona folder)
 * @param marketplaceName - Which marketplace the plugin comes from.
 *   Defaults to the local marketplace for Haephestos-generated plugins.
 *   For predefined role plugins, pass the official marketplace name.
 */
export async function installPluginLocally(
  pluginName: string,
  agentDir: string,
  marketplaceName: string = MARKETPLACE_NAME,
): Promise<void> {
  // Validate plugin name
  if (!/^[a-zA-Z0-9][a-zA-Z0-9_-]*$/.test(pluginName)) {
    throw new Error(`Invalid plugin name "${pluginName}"`)
  }

  // Resolve ~ in agentDir
  const resolvedDir = agentDir.startsWith('~')
    ? agentDir.replace('~', HOME)
    : agentDir

  // Reject path traversal in agentDir
  if (resolvedDir.includes('..')) {
    throw new Error('agentDir must not contain ".."')
  }

  const pluginKey = `${pluginName}@${marketplaceName}`
  const claudeDir = join(resolvedDir, '.claude')
  const localSettings = join(claudeDir, 'settings.local.json')

  // Create .claude directory in agent's project
  await mkdir(claudeDir, { recursive: true })

  // Read or create settings.local.json
  const settings = await loadJsonSafe(localSettings) as Record<string, Record<string, unknown>>
  const ep = (settings.enabledPlugins || {}) as Record<string, boolean>
  ep[pluginKey] = true
  settings.enabledPlugins = ep
  await saveJsonSafe(localSettings, settings)

  // Track in global installed_plugins.json
  await mkdir(join(CLAUDE_DIR, 'plugins'), { recursive: true })
  const installed = await loadJsonSafe(INSTALLED_FILE) as Record<string, unknown>
  const pluginsMap = (installed.plugins || {}) as Record<string, unknown>
  const now = new Date().toISOString().replace('+00:00', 'Z')
  pluginsMap[pluginKey] = [{
    scope: 'local',
    version: '1.0.0',
    installedAt: now,
    lastUpdated: now,
    installPath: join(PLUGINS_DIR, pluginName),
    projectPath: resolvedDir,
  }]
  installed.plugins = pluginsMap
  await saveJsonSafe(INSTALLED_FILE, installed)
}

/**
 * Uninstall a role-plugin from an agent's working directory.
 */
export async function uninstallPluginLocally(
  pluginName: string,
  agentDir: string,
  marketplaceName: string = MARKETPLACE_NAME,
): Promise<void> {
  if (!/^[a-zA-Z0-9][a-zA-Z0-9_-]*$/.test(pluginName)) {
    throw new Error(`Invalid plugin name "${pluginName}"`)
  }

  const resolvedDir = agentDir.startsWith('~')
    ? agentDir.replace('~', HOME)
    : agentDir

  if (resolvedDir.includes('..')) {
    throw new Error('agentDir must not contain ".."')
  }

  const pluginKey = `${pluginName}@${marketplaceName}`
  const localSettings = join(resolvedDir, '.claude', 'settings.local.json')

  if (existsSync(localSettings)) {
    const settings = await loadJsonSafe(localSettings) as Record<string, Record<string, unknown>>
    const ep = (settings.enabledPlugins || {}) as Record<string, boolean>
    delete ep[pluginKey]
    settings.enabledPlugins = ep
    await saveJsonSafe(localSettings, settings)
  }

  // Remove from installed_plugins.json
  if (existsSync(INSTALLED_FILE)) {
    const installed = await loadJsonSafe(INSTALLED_FILE) as Record<string, unknown>
    const pluginsMap = (installed.plugins || {}) as Record<string, unknown>
    delete pluginsMap[pluginKey]
    installed.plugins = pluginsMap
    await saveJsonSafe(INSTALLED_FILE, installed)
  }
}

// ── Listing ────────────────────────────────────────────────

/**
 * List all available role plugins in the marketplace.
 */
export async function listRolePlugins(): Promise<RolePlugin[]> {
  if (!existsSync(PLUGINS_DIR)) return []

  const entries = await readdir(PLUGINS_DIR)
  const plugins: RolePlugin[] = []

  for (const entry of entries) {
    const pluginDir = join(PLUGINS_DIR, entry)
    const pluginJsonPath = join(pluginDir, '.claude-plugin', 'plugin.json')
    const tomlPath = join(pluginDir, `${entry}.agent.toml`)

    const entryStat = await stat(pluginDir).catch(() => null)
    if (!entryStat?.isDirectory()) continue
    if (!existsSync(pluginJsonPath)) continue

    try {
      const meta = JSON.parse(await readFile(pluginJsonPath, 'utf-8'))
      let model: string | undefined
      let program: string | undefined

      // Try to extract model/program from the saved TOML
      if (existsSync(tomlPath)) {
        try {
          const tomlContent = await readFile(tomlPath, 'utf-8')
          const parsed = parseAgentToml(tomlContent)
          model = parsed.agent.model
          program = parsed.agent.program
        } catch { /* ignore parse errors */ }
      }

      plugins.push({
        name: meta.name || entry,
        version: meta.version || '1.0.0',
        description: meta.description || '',
        model,
        program,
        pluginDir,
      })
    } catch { /* skip malformed plugins */ }
  }

  return plugins
}

/**
 * Delete a role plugin from the marketplace.
 */
export async function deleteRolePlugin(pluginName: string): Promise<void> {
  if (!/^[a-zA-Z0-9][a-zA-Z0-9_-]*$/.test(pluginName)) {
    throw new Error(`Invalid plugin name "${pluginName}"`)
  }

  const pluginDir = join(PLUGINS_DIR, pluginName)
  if (existsSync(pluginDir)) {
    await rm(pluginDir, { recursive: true })
  }

  // Remove from marketplace manifest
  if (existsSync(MARKETPLACE_JSON)) {
    const manifest = await loadJsonSafe(MARKETPLACE_JSON) as Record<string, unknown>
    const plugins = (manifest.plugins || []) as Array<Record<string, string>>
    manifest.plugins = plugins.filter(p => p.name !== pluginName)
    await writeFile(MARKETPLACE_JSON, JSON.stringify(manifest, null, 2) + '\n')
  }

  // Remove from global enabledPlugins
  const pluginKey = `${pluginName}@${MARKETPLACE_NAME}`
  if (existsSync(SETTINGS_LOCAL)) {
    const settings = await loadJsonSafe(SETTINGS_LOCAL) as Record<string, Record<string, unknown>>
    const ep = (settings.enabledPlugins || {}) as Record<string, boolean>
    delete ep[pluginKey]
    settings.enabledPlugins = ep
    await saveJsonSafe(SETTINGS_LOCAL, settings)
  }
}

