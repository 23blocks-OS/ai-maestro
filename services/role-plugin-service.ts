export interface SyncDefaultsResult {
  synced: string[]
  skipped: string[]
  errors: Array<{ name: string; error: string }>
}

/**
 * Download the 6 default role plugins from GitHub into the local marketplace.
 * Skips plugins that already exist (have a valid plugin.json).
 * Uses `git clone --depth 1` for minimal bandwidth, then removes .git/.
 *
 * @param force - If true, re-downloads even if the plugin already exists
 */
export async function syncDefaultRolePlugins(force = false): Promise<SyncDefaultsResult> {
  await ensureMarketplace()

  const synced: string[] = []
  const skipped: string[] = []
  const errors: Array<{ name: string; error: string }> = []

  for (const [pluginName, { repo, description }] of Object.entries(DEFAULT_ROLE_PLUGIN_REPOS)) {
    const pluginDir = join(PLUGINS_DIR, pluginName)
    const pluginJsonPath = join(pluginDir, '.claude-plugin', 'plugin.json')

    // Skip if already exists and has plugin.json (unless force)
    if (!force && existsSync(pluginJsonPath)) {
      skipped.push(pluginName)
      // Ensure it's registered in the manifest even if skipped
      try {
        const meta = JSON.parse(await readFile(pluginJsonPath, 'utf-8'))
        await updateMarketplaceManifest(pluginName, meta.description || description, meta.version || '1.0.0')
      } catch { /* ignore */ }
      continue
    }

    try {
      // Clean target dir if it exists but is incomplete
      if (existsSync(pluginDir)) {
        await rm(pluginDir, { recursive: true })
      }

      // Clone the repo with depth 1 (minimal download)
      await execFileAsync('git', [
        'clone', '--depth', '1',
        `https://github.com/${repo}.git`,
        pluginDir,
      ], { timeout: 120000 })

      // Remove .git directory — not needed in the marketplace
      const gitDir = join(pluginDir, '.git')
      if (existsSync(gitDir)) {
        await rm(gitDir, { recursive: true })
      }

      // Read plugin.json to get actual description/version
      if (existsSync(pluginJsonPath)) {
        const meta = JSON.parse(await readFile(pluginJsonPath, 'utf-8'))
        await updateMarketplaceManifest(
          pluginName,
          meta.description || description,
          meta.version || '1.0.0',
        )
      } else {
        await updateMarketplaceManifest(pluginName, description, '1.0.0')
      }

      synced.push(pluginName)
      console.log(`[role-plugins] Synced default: ${pluginName}`)
    } catch (err) {
      const msg = err instanceof Error ? err.message : String(err)
      errors.push({ name: pluginName, error: msg })
      console.error(`[role-plugins] Failed to sync ${pluginName}:`, msg)
    }
  }

  return { synced, skipped, errors }
}

export interface CreatePersonaResult {
  personaName: string
  agentDir: string
  pluginName: string
  mainAgentName: string
  marketplaceName: string
}

/**
 * Unified persona creation — used by both the wizard (predefined role plugins)
 * and Haephestos (custom TOML-generated plugins).
 *
 * Exactly one of `tomlContent` or `pluginName` must be provided:
 * - tomlContent: Haephestos flow — generates a new plugin, adds to local marketplace
 * - pluginName: Wizard flow — uses an existing predefined role plugin
 *
 * In both cases, creates ~/agents/<personaName>/ and installs the plugin
 * with --scope local into that folder.
 */
export async function createPersona(opts: {
  personaName: string
  tomlContent?: string
  pluginName?: string
  marketplaceName?: string
  agentDescription?: string
}): Promise<CreatePersonaResult> {
  const { personaName, tomlContent, agentDescription } = opts

  // Validate persona name: lowercase alphanumeric + hyphens
  if (!/^[a-z0-9]([a-z0-9-]*[a-z0-9])?$/.test(personaName)) {
    throw new Error('personaName must be lowercase alphanumeric with hyphens (e.g. "peter-bot")')
  }

  // Must provide exactly one source
  if (tomlContent && opts.pluginName) {
    throw new Error('Provide either tomlContent or pluginName, not both')
  }
  if (!tomlContent && !opts.pluginName) {
    throw new Error('Either tomlContent or pluginName is required')
  }

  let pluginName: string
  let mainAgentName: string
  let marketplaceName: string

  if (tomlContent) {
    // Haephestos flow: generate plugin from TOML → local marketplace
    const result = await generatePluginFromToml(tomlContent, agentDescription)
    pluginName = result.pluginName
    mainAgentName = result.mainAgentName
    marketplaceName = MARKETPLACE_NAME
  } else {
    // Wizard flow: use existing predefined plugin
    pluginName = opts.pluginName!
    const predefined = PREDEFINED_ROLE_PLUGINS[pluginName]
    if (predefined) {
      marketplaceName = opts.marketplaceName || predefined.marketplace
      mainAgentName = predefined.mainAgent
    } else {
      // Custom plugin name — caller must specify marketplace
      marketplaceName = opts.marketplaceName || MARKETPLACE_NAME
      mainAgentName = `${pluginName}-main-agent`
    }
  }

  // Create the persona folder: ~/agents/<personaName>/
  const agentDir = join(HOME, 'agents', personaName)
  await mkdir(agentDir, { recursive: true })

  // Install the plugin locally into the persona folder
  await installPluginLocally(pluginName, agentDir, marketplaceName)

  return {
    personaName,
    agentDir,
    pluginName,
    mainAgentName,
    marketplaceName,
  }
}
