import { existsSync } from 'fs'
import { execFile } from 'child_process'
import { promisify } from 'util'

const execFileAsync = promisify(execFile)

// ── Paths ──────────────────────────────────────────────────
const HOME = homedir()
const ROLE_PLUGINS_DIR = join(HOME, 'agents', 'role-plugins')
const MARKETPLACE_META_DIR = join(ROLE_PLUGINS_DIR, '.claude-plugin')
const MARKETPLACE_JSON = join(MARKETPLACE_META_DIR, 'marketplace.json')
const PLUGINS_DIR = join(ROLE_PLUGINS_DIR, 'plugins')
const CLAUDE_DIR = join(HOME, '.claude')
const SETTINGS_LOCAL = join(CLAUDE_DIR, 'settings.local.json')
const INSTALLED_FILE = join(CLAUDE_DIR, 'plugins', 'installed_plugins.json')

const MARKETPLACE_NAME = 'ai-maestro-local-roles-marketplace'

// ── Types ──────────────────────────────────────────────────

interface TomlAgent {
  name: string
  program?: string
  model?: string
  workingDirectory?: string
  description?: string
  teamId?: string
}

interface TomlSkills {
  primary?: string[]
  secondary?: string[]
  specialized?: string[]
}

interface TomlDependencies {
  plugins?: string[]
  skills?: string[]
  rules?: string[]
  agents?: string[]
  commands?: string[]
  hooks?: string[]
  mcp_servers?: string[]
  lsp_servers?: string[]
  output_styles?: string[]
  tools?: string[]
  frameworks?: string[]
}

interface TomlRules {
  items?: string[]
  recommended?: string[]
}

interface TomlRecommended {
  recommended?: string[]
}

interface ParsedToml {
  agent: TomlAgent
  skills?: TomlSkills
  dependencies?: TomlDependencies
  rules?: TomlRules
  agents?: TomlRecommended
  commands?: TomlRecommended
  hooks?: TomlRecommended
  mcp?: TomlRecommended
  lsp?: TomlRecommended
  output_styles?: TomlRecommended
  description?: { text?: string }
}

export interface RolePlugin {
  name: string
  version: string
  description: string
  model?: string
  program?: string
  pluginDir: string
}

// ── JSON helpers ───────────────────────────────────────────

async function loadJsonSafe(path: string): Promise<Record<string, unknown>> {
  if (!existsSync(path)) return {}
  try {
    const raw = await readFile(path, 'utf-8')
    return JSON.parse(raw)
  } catch {
    return {}
  }
}

async function saveJsonSafe(path: string, data: Record<string, unknown>): Promise<void> {
  const dir = join(path, '..')
  await mkdir(dir, { recursive: true })
  await writeFile(path, JSON.stringify(data, null, 2) + '\n', 'utf-8')
}

// ── TOML parsing ───────────────────────────────────────────

function parseAgentToml(tomlContent: string): ParsedToml {
  const parsed = parseToml(tomlContent) as Record<string, unknown>

  const agentSection = (parsed.agent || {}) as Record<string, unknown>
  if (!agentSection.name || typeof agentSection.name !== 'string') {
    throw new Error('TOML must have [agent] section with a name field')
  }

  return {
    agent: {
      name: agentSection.name as string,
      program: (agentSection.program as string) || 'claude-code',
      model: agentSection.model as string | undefined,
      workingDirectory: agentSection.workingDirectory as string | undefined,
      description: agentSection.description as string | undefined,
      teamId: agentSection.teamId as string | undefined,
    },
    skills: parsed.skills as TomlSkills | undefined,
    dependencies: parsed.dependencies as TomlDependencies | undefined,
    rules: parsed.rules as TomlRules | undefined,
    agents: parsed.agents as TomlRecommended | undefined,
    commands: parsed.commands as TomlRecommended | undefined,
    hooks: parsed.hooks as TomlRecommended | undefined,
    mcp: parsed.mcp as TomlRecommended | undefined,
    lsp: parsed.lsp as TomlRecommended | undefined,
    output_styles: parsed.output_styles as TomlRecommended | undefined,
    description: parsed.description as { text?: string } | undefined,
  }
}

// ── Plugin generation ──────────────────────────────────────

/**
 * Generate a Claude Code plugin from a .agent.toml profile.
 * Saves it to ~/agents/role-plugins/plugins/<name>/
 */
export async function generatePluginFromToml(
  tomlContent: string,
  agentDescription?: string,
): Promise<{ pluginName: string; pluginDir: string; mainAgentName: string }> {
  const config = parseAgentToml(tomlContent)
  const { agent } = config
  const pluginName = agent.name

  // Validate plugin name to prevent path traversal
  if (!/^[a-zA-Z0-9][a-zA-Z0-9_-]*$/.test(pluginName)) {
    throw new Error(`Invalid agent name "${pluginName}": must be alphanumeric with hyphens/underscores, no path separators`)
  }

  const pluginDir = join(PLUGINS_DIR, pluginName)

  // Wipe existing plugin dir if it exists (regenerating)
  if (existsSync(pluginDir)) {
    await rm(pluginDir, { recursive: true })
  }

  // Create plugin directory structure
  const metaDir = join(pluginDir, '.claude-plugin')
  const agentsDir = join(pluginDir, 'agents')
  await mkdir(metaDir, { recursive: true })
  await mkdir(agentsDir, { recursive: true })

  // 1. plugin.json — prefer [description].text, fall back to [agent].description
  const pluginDescription = config.description?.text || agent.description || `Role plugin for ${pluginName}`
  const pluginJson = {
    name: pluginName,
    version: '1.0.0',
    description: pluginDescription,
  }
  await writeFile(join(metaDir, 'plugin.json'), JSON.stringify(pluginJson, null, 2) + '\n')

  // 2. agents/<plugin-name>-main-agent.md — the persona file
  // Follows the quad-match rule from the SKILL.md: agents/<plugin-name>-main-agent.md
  const mainAgentName = `${pluginName}-main-agent`
  const personaMd = buildPersonaMarkdown(mainAgentName, agent, config, agentDescription)
  await writeFile(join(agentsDir, `${mainAgentName}.md`), personaMd)

  // 3. Rules are NOT copied into the plugin.
  // Global rules (~/.claude/rules/) are auto-loaded by Claude Code for every session.
  // Project-level rules (.claude/rules/) are auto-loaded per project.
  // The [rules].recommended section in the TOML is metadata only (records which
  // rules the PSS profiler selected). No file copying needed — the plugin
  // installation with --scope local handles everything automatically.

  // 4. Save the original .agent.toml inside the plugin (quad-match: <plugin-name>.agent.toml)
  await writeFile(join(pluginDir, `${pluginName}.agent.toml`), tomlContent)

  // 5. Verify the plugin was created correctly by reading back the actual plugin.json
  // This guards against any discrepancy between what we intended and what exists on disk.
  const actualPluginJsonPath = join(metaDir, 'plugin.json')
  if (!existsSync(actualPluginJsonPath)) {
    throw new Error(`Plugin creation failed: ${actualPluginJsonPath} does not exist after writing`)
  }
  const actualPluginJson = JSON.parse(await readFile(actualPluginJsonPath, 'utf-8')) as Record<string, string>
  const actualName = actualPluginJson.name
  const actualDescription = actualPluginJson.description || ''
  const actualVersion = actualPluginJson.version || '1.0.0'

  // Verify quad-match: plugin.json name must equal directory name
  const dirName = pluginDir.split('/').pop()
  if (actualName !== dirName) {
    // Fix the mismatch — plugin.json name is the source of truth for the marketplace
    console.warn(`[role-plugin] Quad-match fix: plugin.json name "${actualName}" != dir name "${dirName}". Updating plugin.json.`)
    actualPluginJson.name = dirName!
    await writeFile(actualPluginJsonPath, JSON.stringify(actualPluginJson, null, 2) + '\n')
  }

  // Verify main-agent.md exists
  const mainAgentPath = join(agentsDir, `${mainAgentName}.md`)
  if (!existsSync(mainAgentPath)) {
    throw new Error(`Plugin creation failed: ${mainAgentPath} does not exist after writing`)
  }

  // 6. NOW register in marketplace — only after plugin is fully created and verified
  await ensureMarketplace()
  await updateMarketplaceManifest(
    actualPluginJson.name || pluginName,
    actualDescription,
    actualVersion,
  )

  return { pluginName, pluginDir, mainAgentName }
}

function buildPersonaMarkdown(
  mainAgentName: string,
  agent: TomlAgent,
  config: ParsedToml,
  agentDescription?: string,
): string {
  const { skills, dependencies } = config
  const lines: string[] = []

  const descText = config.description?.text || agent.description || `${agent.name} role agent`

  // Frontmatter — name must match the filename (quad-match rule)
  lines.push('---')
  lines.push(`name: ${mainAgentName}`)
  lines.push(`description: ${descText}`)
  if (agent.model) lines.push(`model: ${agent.model}`)
  lines.push('---')
  lines.push('')

  // Title and description
  lines.push(`# ${agent.name}`)
  lines.push('')
  if (agentDescription) {
    lines.push(agentDescription)
    lines.push('')
  } else if (config.description?.text) {
    lines.push(config.description.text)
    lines.push('')
  } else if (agent.description) {
    lines.push(agent.description)
    lines.push('')
  }

  // Skills section
  const allSkills = [
    ...(skills?.primary || []),
    ...(skills?.secondary || []),
    ...(skills?.specialized || []),
  ]
  if (allSkills.length > 0) {
    lines.push('## Skills')
    lines.push('')
    if (skills?.primary?.length) {
      lines.push('### Primary')
      skills.primary.forEach(s => lines.push(`- ${s}`))
      lines.push('')
    }
    if (skills?.secondary?.length) {
      lines.push('### Secondary')
      skills.secondary.forEach(s => lines.push(`- ${s}`))
      lines.push('')
    }
    if (skills?.specialized?.length) {
      lines.push('### Specialized')
      skills.specialized.forEach(s => lines.push(`- ${s}`))
      lines.push('')
    }
  }

  // Rules
  const rules = config.rules?.recommended || config.rules?.items || []
  if (rules.length > 0) {
    lines.push('## Rules')
    lines.push('')
    rules.forEach(r => lines.push(`- ${r}`))
    lines.push('')
  }

  // Sub-agents
  if (config.agents?.recommended?.length) {
    lines.push('## Agents')
    lines.push('')
    config.agents.recommended.forEach(a => lines.push(`- ${a}`))
    lines.push('')
  }

  // Commands
  if (config.commands?.recommended?.length) {
    lines.push('## Commands')
    lines.push('')
    config.commands.recommended.forEach(c => lines.push(`- /${c}`))
    lines.push('')
  }

  // Hooks
  if (config.hooks?.recommended?.length) {
    lines.push('## Hooks')
    lines.push('')
    config.hooks.recommended.forEach(h => lines.push(`- ${h}`))
    lines.push('')
  }

  // MCP Servers
  if (config.mcp?.recommended?.length) {
    lines.push('## MCP Servers')
    lines.push('')
    config.mcp.recommended.forEach(m => lines.push(`- ${m}`))
    lines.push('')
  }

  // LSP Servers
  if (config.lsp?.recommended?.length) {
    lines.push('## LSP Servers')
    lines.push('')
    config.lsp.recommended.forEach(l => lines.push(`- ${l}`))
    lines.push('')
  }

  // Output Styles
  if (config.output_styles?.recommended?.length) {
    lines.push('## Output Styles')
    lines.push('')
    config.output_styles.recommended.forEach(o => lines.push(`- ${o}`))
    lines.push('')
  }

  // Dependencies (user-scope prerequisites)
  if (dependencies) {
    lines.push('## Dependencies')
    lines.push('')
    const depEntries: [string, string[] | undefined][] = [
      ['Plugins', dependencies.plugins],
      ['Skills', dependencies.skills],
      ['Rules', dependencies.rules],
      ['Agents', dependencies.agents],
      ['Commands', dependencies.commands],
      ['Hooks', dependencies.hooks],
      ['MCP Servers', dependencies.mcp_servers],
      ['LSP Servers', dependencies.lsp_servers],
      ['Output Styles', dependencies.output_styles],
      ['Tools', dependencies.tools],
      ['Frameworks', dependencies.frameworks],
    ]
    for (const [label, items] of depEntries) {
      if (items?.length) {
        lines.push(`- **${label}:** ${items.join(', ')}`)
      }
    }
    lines.push('')
  }

  return lines.join('\n')
}

// ── Marketplace management ─────────────────────────────────

/**
 * Ensure the role-plugins marketplace directory and global registration exist.
 */
async function ensureMarketplace(): Promise<void> {
  // Create marketplace structure
  await mkdir(MARKETPLACE_META_DIR, { recursive: true })
  await mkdir(PLUGINS_DIR, { recursive: true })

  // Create marketplace.json if it doesn't exist
  if (!existsSync(MARKETPLACE_JSON)) {
    const manifest = {
      name: MARKETPLACE_NAME,
      version: '1.0.0',
      owner: { name: 'local' },
      metadata: {
        description: 'Local role-plugin marketplace managed by AI Maestro',
      },
      plugins: [],
    }
    await writeFile(MARKETPLACE_JSON, JSON.stringify(manifest, null, 2) + '\n')
  }

  // Register in global settings.local.json
  await registerMarketplaceGlobally()
}

async function registerMarketplaceGlobally(): Promise<void> {
  const settings = await loadJsonSafe(SETTINGS_LOCAL) as Record<string, Record<string, unknown>>
  const ekm = (settings.extraKnownMarketplaces || {}) as Record<string, unknown>

  // The canonical entry with the correct absolute path
  const canonicalEntry = {
    source: {
      source: 'directory',
      path: ROLE_PLUGINS_DIR,
    },
  }

  // Always ensure the canonical entry has the correct absolute path
  // (fixes corrupted or stale paths from earlier versions)
  const existing = ekm[MARKETPLACE_NAME] as Record<string, Record<string, string>> | undefined
  const existingPath = existing?.source?.path
  if (existingPath === ROLE_PLUGINS_DIR) return

  ekm[MARKETPLACE_NAME] = canonicalEntry
  settings.extraKnownMarketplaces = ekm
  await saveJsonSafe(SETTINGS_LOCAL, settings)
}

async function updateMarketplaceManifest(
  pluginName: string,
  description: string,
  version: string,
): Promise<void> {
  const manifest = await loadJsonSafe(MARKETPLACE_JSON) as Record<string, unknown>
  const plugins = (manifest.plugins || []) as Array<Record<string, string>>

  // Remove existing entry for this plugin (will re-add)
  const filtered = plugins.filter(p => p.name !== pluginName)
  filtered.push({
    name: pluginName,
    description,
    version,
    source: `./plugins/${pluginName}`,
  })
  manifest.plugins = filtered

  await writeFile(MARKETPLACE_JSON, JSON.stringify(manifest, null, 2) + '\n')
}

